package com.solar.tech.bean;

public class Count {
	
	private String EnName;
	private String CnName;
	private String sysCount;
	private String sysListUrl;
	
	
	public String getEnName() {
		return EnName;
	}
	public void setEnName(String enName) {
		EnName = enName;
	}
	public String getCnName() {
		return CnName;
	}
	public void setCnName(String cnName) {
		CnName = cnName;
	}
	public String getSysCount() {
		return sysCount;
	}
	public void setSysCount(String sysCount) {
		this.sysCount = sysCount;
	}
	public String getSysListUrl() {
		return sysListUrl;
	}
	public void setSysListUrl(String sysListUrl) {
		this.sysListUrl = sysListUrl;
	}
	

}
