package com.solar.tech.bean;

public class Element {
	String systemName;
	String systemEnName;
	String systemCnName;
	String title;
	String receiveTime;
	String pendingName;
	String url;
	String count;
	String pageUrl;
	
	public String getSystemName() {
		return systemName;
	}
	public void setSystemName(String systemName) {
		this.systemName = systemName;
	}
	public String getSystemEnName() {
		return systemEnName;
	}
	public void setSystemEnName(String systemEnName) {
		this.systemEnName = systemEnName;
	}
	public String getSystemCnName() {
		return systemCnName;
	}
	public void setSystemCnName(String systemCnName) {
		this.systemCnName = systemCnName;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getReceiveTime() {
		return receiveTime;
	}
	public void setReceiveTime(String receiveTime) {
		this.receiveTime = receiveTime;
	}
	public String getPendingName() {
		return pendingName;
	}
	public void setPendingName(String pendingName) {
		this.pendingName = pendingName;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getPageUrl() {
		return pageUrl;
	}
	public void setPageUrl(String pageUrl) {
		this.pageUrl = pageUrl;
	}
	
}
