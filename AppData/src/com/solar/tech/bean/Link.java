package com.solar.tech.bean;

public class Link {
	//名称（链接名称）
	private String name;
	//链接（名称链接）
	private String url;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
}
